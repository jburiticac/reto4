   public void actualizarEmpleado(){
         int idEmp= Integer.parseInt(txtSucursal.getText());
         String documento=txtDocumento.getText();
         String nombre=txtNombre.getText();
         String apellidos=txtApellido.getText();
         String correo=txtCorreo.getText();
         
         if(nombre.isEmpty()){
           JOptionPane.showMessageDialog(this,"el nombre es un campo requerido","",JOptionPane.WARNING_MESSAGE);
         
         
         }else if(apellidos.isEmpty()){
           JOptionPane.showMessageDialog(this,"el apellido es un campo requerido","",JOptionPane.WARNING_MESSAGE);
         
         }else if(correo.isEmpty()){
           JOptionPane.showMessageDialog(this,"el nombre es un campo requerido","",JOptionPane.WARNING_MESSAGE);
   
         }else{
         
         String query ="UPDATE `empleado` SET `nombreEmp`='"+ nombre +"',`apellidos`='" + apellidos + "',`correo`='"+correo + "'"
                 + " WHERE idEmp =" + idEmp + ";";
         
             try {
                 connection= conexion.getCConnection();
                st=connection.createStatement();
                st.executeUpdate(query);
             JOptionPane.showMessageDialog(this, "se ha creado el nuevo usuario");
            
                 
             } catch (SQLException e) {
                 JOptionPane.showMessageDialog(this,"no fue posible actualizar los datos","",JOptionPane.ERROR_MESSAGE);
             }
             
             this.dispose();
         
         }
   
   }
   public void EliminarEmpleado(){
       int idEmp= Integer.parseInt(txtSucursal.getText());
       String query="DELETE FROM `empleado` WHERE idEmp ="+idEmp+";";
        try {
                 connection= conexion.getCConnection();
                st=connection.createStatement();
                st.executeUpdate(query);
             JOptionPane.showMessageDialog(this, "El usuario ha sido eliminado");
            
                 
             } catch (SQLException e) {
                 JOptionPane.showMessageDialog(this,"no se pudo eliminar el emplead","",JOptionPane.ERROR_MESSAGE);
             }
             
             this.dispose();
   
   
   }
    
    
