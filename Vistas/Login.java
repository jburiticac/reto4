    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String Usuario,contraseña;
        Usuario = txtUsername.getText();
        contraseña = txtPassword.getText();
        System.out.println("usuario:" + Usuario + ", contraseña:  " + contraseña);
        
         //validar los campos de login
         if(Usuario.isEmpty()|| contraseña.isEmpty()){
             JOptionPane.showMessageDialog(this, "Fatan Campos Por Diligenciar", Usuario, JOptionPane.WARNING_MESSAGE);
         
         }else{
             if (Usuario.equals("MisionTic") && contraseña.equals("Ciclo2")){
                JOptionPane.showMessageDialog(this, "Bienvenido");
                UserMenu userMenu = new UserMenu();
                userMenu.setVisible(true);
                
                
                
                this.dispose();
                       
             
 
             }else{
                 JOptionPane.showMessageDialog(this, "no se pudo iniciar seccion",Usuario,JOptionPane.ERROR_MESSAGE);
             }
         
         }
         
                
                
    }//GEN-LAST:event_btnLoginActionPerformed

